    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
     try {
        int id = Integer.parseInt(txtId.getText());
        String name = txtName.getText();
        float expensiveTeam = Float.parseFloat(txtExpensive.getText());
        String signing = txtSigninig.getText();
        float transferPrice = Float.parseFloat(txtTransfer.getText());

        MongoDatabase database = MongoConnection.getDatabase();
        MongoCollection<Document> collection = database.getCollection("Teams");

        // Verificar si el ID ya existe
        Document existing = collection.find(new Document("id", id)).first();
        if (existing != null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ya existe un equipo con ese ID.");
            return;
        }

        Document newTeam = new Document("id", id)
                .append("name", name)
                .append("ExpensiveTeam", expensiveTeam)
                .append("MostExpensiveSigning", signing)
                .append("TransferPrice", transferPrice);

        collection.insertOne(newTeam);
        javax.swing.JOptionPane.showMessageDialog(this, "Equipo agregado exitosamente.");
        //loadTeamsToTable();
        //clearFields();

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor ingrese datos v√°lidos.");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al agregar: " + e.getMessage());
    }

    }//GEN-LAST:event_btnAddActionPerformed

